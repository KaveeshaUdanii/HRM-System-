    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Get the entered username and password
        String enteredUsername = jTextField1.getText();
        String enteredPassword = jPasswordField1.getText();

        // Check if the text fields are empty
        if (enteredUsername.isEmpty() || enteredPassword.isEmpty()) {
            // If one or both text fields are empty, display an error message
            JOptionPane.showMessageDialog(this, "Please enter both username and password.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method to prevent further execution
        }

        // Check the credentials
        if (checkCredentials(enteredUsername, enteredPassword)) {
            // If the credentials are correct, display a success message
            JOptionPane.showMessageDialog(this, "Login successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
            // Clear the text fields
            jTextField1.setText("");
            jPasswordField1.setText("");
        } else {
            // If the credentials are incorrect, display an error message
            JOptionPane.showMessageDialog(this, "Incorrect username or password. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

