    private boolean checkCredentials(String enteredUsername, String enteredPassword) {
        // Read the contents of the HR.txt file and check if the credentials match
        try (Scanner scanner = new Scanner(new File("Assistant.txt"))) {
            while (scanner.hasNextLine()) {
                String line = scanner.nextLine();
                if (line.startsWith("Username:")) {
                    String usernameFromFile = line.substring("Username:".length()).trim();
                    if (!scanner.hasNextLine()) {
                        // If there's no more line after the username, return false
                        return false;
                    }
                    line = scanner.nextLine();
                    if (!line.startsWith("Password:")) {
                        // If the next line doesn't start with "Password:", return false
                        return false;
                    }
                    String passwordFromFile = line.substring("Password:".length()).trim();
                    if (enteredUsername.equals(usernameFromFile) && enteredPassword.equals(passwordFromFile)) {
                        return true; // Credentials match
                    }
                }
            }
        } catch (FileNotFoundException e) {
            // Handle file not found exception
            e.printStackTrace();
        }
        return false; // No match found
    }
